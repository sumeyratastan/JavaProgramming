package week04;

public class TrafficLightSelecter {
    public static void main(String[] args) {
        char colour='r';

        switch (colour){
            case 'R': case 'r':
                System.out.println("red light");
                break;

            case 'O': case 'o':
                System.out.println("orange light");
                break;

            case 'G': case 'g':
                System.out.println("green light");
                break;

            default:
                System.out.println(" invalid");





        }





    }
}
