package day09_IfStatements;

public class PosNegZero {
    public static void main(String[] args) {

        int n = -7;

        if (n>0){
            System.out.println(n + " is positive");
        }else if (n<0){
            System.out.println(n + " is negative");
        }else{
            System.out.println(n + " is zero");
        }






    }
}
